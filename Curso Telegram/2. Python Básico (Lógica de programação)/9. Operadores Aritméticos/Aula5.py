# Aula 5, operadores aritiméticos


'''

+ - Adição
- - Subtração
* - Multiplicação
/ - Divisão
// - Divisão inteira
** - Potenciação
% - Resto da divisão
() - Precedência

'''

print("1 - Adição - ", "Resposta: 123 + 456 = ", 123+456)
print("2 - Subtração - ", "Resposta: 456 - 123 = ", 456-123)
print("3 - Multiplicação - ", "Resposta: 123 * 456 = ", 123*456)
print("4 - Divisão - ", "Resposta: 456 / 123 = ", 456/123)
print(f'5 - Divisão inteira - Resposta: 456 // 132 = {456//123}')
print(f'6 - Potenciação - Resposta: 123 ** 456 = {123**456}')
print(f'7 - Resto da divisão - módulo - Resposta: 10 % 3 = {10%3}')